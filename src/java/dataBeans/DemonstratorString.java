package dataBeans;

/**
 *
 * @author Nikola
 */
public class DemonstratorString {

    private String userName;
    private String password;
    private String name;
    private String surname;
    private String telNumber;
    private String email;
    private String image;
    private String odsek;
    private String godina;
    private String prosek;
    
    public DemonstratorString() {}

    public String getUserName() {
        return userName;
    }

    public String getPassword() {
        return password;
    }

    public String getName() {
        return name;
    }

    public String getSurname() {
        return surname;
    }

    public String getTelNumber() {
        return telNumber;
    }

    public String getEmail() {
        return email;
    }

    public String getImage() {
        return image;
    }

    public String getOdsek() {
        return odsek;
    }

    public String getGodina() {
        return godina;
    }

    public String getProsek() {
        return prosek;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setSurname(String surname) {
        this.surname = surname;
    }

    public void setTelNumber(String telNumber) {
        this.telNumber = telNumber;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public void setOdsek(String odsek) {
        this.odsek = odsek;
    }

    public void setGodina(String godina) {
        this.godina = godina;
    }

    public void setProsek(String prosek) {
        this.prosek = prosek;
    }
    
    

    
}
